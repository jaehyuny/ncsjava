package firstpackage;

public class CasePractice {
	public static void main(String[] args) {
		int age = 1000;
		switch(age/50) {
			case 10:
				System.out.println("10 대");
				break;
			case 20:
				System.out.println("20 대");
				break;
			case 30:
				System.out.println("30 대");
				break;
			case 40:
				System.out.println("40 대");
				break;
		}
	}
}
